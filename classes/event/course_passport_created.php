namespace local_onlineeduru\event;

defined('MOODLE_INTERNAL') || die();

class course_passport_created extends \core\event\base {

    /**
     * Set basic properties for the event.
     */
    protected function init() {
        $this->data['objecttable'] = 'local_onlineeduru_passport';
        $this->data['crud'] = 'r';
        $this->data['edulevel'] = self::LEVEL_OTHER;
    }

    /**
     * Returns localised general event name.
     *
     * @return string
     */
    public static function get_name() {
        return get_string('course_passport_created', 'local_onlineeduru');
    }

    /**
     * Get URL related to the action.
     *
     * @return \moodle_url
     */
    public function get_url() {
        return new \moodle_url('/local/onlineeduru/view.php', array('id' => $this->contextinstanceid));
    }
}

